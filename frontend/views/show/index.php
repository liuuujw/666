                        <p>仰望布达拉</p>
                    </div>
                    <div class="article-content">
                        <p>我最早听说布达拉宫都是在歌里，歌声动听悠扬，我想去哪里亲眼看看。这回我来到了这里。</p>
                        <p>
                            布达拉宫坐落在拉萨的一座山上，它分为红宫和白宫，一共有13层，是一个很大的宫殿群。远远望去，高大雄伟、金碧辉煌。</p>
                        <p>
                            我们沿着宫墙往上爬，走着走着，忽然闻见了一股淡淡的奶香味儿，后面有一个导游说，这是用牛奶加石灰，每年泼一次。我心想：都来了，就尝一口吧。我伸出舌头优雅斯文的舔了一口，果真是一股香甜的牛奶味儿。在城墙上盖了一层白马草，刷成深红色，配上白色墙，感觉很美观。白马草是喇嘛告诉我的，它长在海拔5000米以上。</p>
                        <p>
                            从城墙往外看，就可以看见整个的拉萨市，看起来很壮观。布达拉宫从前是达赖喇嘛的冬宫，上四层是红色的，也就是达赖喇嘛住的，里面布置的富丽堂皇，基本上全是金子做的，看得我好急呀！有好多金佛、经书，五世达赖的灵塔就用了3700公斤黄金和几万颗宝石。下几层是达赖的小喇嘛、小喽啰、卫兵等仆人住的地方。有一个地方现在改建了珍宝馆，那里有许许多多的布达拉宫的宝贝，有几百种，做的小人千姿百态、刀枪千奇百种、瓶子形态万千，不论什么东西都很精致。</p>
                        <p>
                            布达拉宫真是金子的天堂。</p>
