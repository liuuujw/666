                        <p>孟姜女哭长城</p>
                    </div>
                    <div class="article-content">
                        <p>这个故事发生在很久很久以前，那时候秦始皇正徵发八十万民工修筑万里长城。官府到处抓人去当民工，被抓去的人不分白天黑夜地修筑长城，不知累死了多少。</p>
                        <p>
                            苏州有个书生叫范杞梁，为了逃避官府的追捕，他不得不四处躲藏。有一天，他逃到了孟家花园，无意中遇到了孟姜女。孟姜女是一个聪明美丽的姑娘，她和父母一起把范杞梁藏了起来。两位老人很喜欢范杞梁，就把孟姜女许配给他作了妻子。</p>
                        <p>
                            新婚不到三天，范杞梁就被公差抓去修长城了。孟姜女哭得象泪人似的，苦苦地等待丈夫归来。半年过去了，范杞梁一点消息也没有。这时已是深秋季节，北风四起，芦花泛白，天气一天比一天冷了。孟姜女想起丈夫远在北方修长城，一定十分寒冷，就亲手缝制了寒衣，启程上路，要到万里长城去寻找范杞梁。</p>
                        <p>
                            一路上，孟姜女不知经历了多少艰难，吃了多少苦，才来到了长城脚下。谁知修长城的民工告诉她，范杞梁已经死了，尸骨被填进了城墙里。听到这个令人心碎的消息，孟姜女只觉得天昏地暗，一下子昏倒在地，醒来后，她伤心地痛哭起来，只哭得天愁地惨，日月无光。不知哭了多久，忽听得天摇地动般地一声巨响，长城崩塌了几十里，露出了数不清的尸骨。孟姜女咬破手指，把血滴在一具具的尸骨上，她心里暗暗祷告:如果是丈夫的尸骨，血就会渗进骨头，如果不是，血就会流向四方。终于，孟姜女用这种方法找到了范杞梁的尸骨。她抱着这堆白骨，又伤心地痛哭起来。</p>
                        <p>
                            秦始皇看到孟姜女很美丽，想逼她做妃子。孟姜女假意答应了他，但要求秦始皇先办三件事:请和尚给范杞梁念四十九天经，然后把他好好地埋葬;秦始皇要亲自率文武大臣哭祭范杞梁;埋葬范杞梁后，孟姜女要去游山玩水，三天以后才能成亲。秦始皇只得答应了孟姜女的要求。三件事办完以后，孟姜女把秦始皇痛骂了一顿，然后纵身跳进了波涛滚滚的大海。</p>
                    </div>
